package main

import (
	"cfa-suite/src/types"
	"log"

	"github.com/gin-gonic/gin"
	"github.com/joho/godotenv"
)

func main() {

	//==========================================================================
	// CONFIG
	//==========================================================================

	_ = godotenv.Load()

	
	//==========================================================================
	// DATABASE
	//==========================================================================

	database := types.NewDatabase()
	err := database.InitTables()
	if err != nil {
		log.Fatal(err.Error())
	}

	//==========================================================================
	// ROUTER
	//==========================================================================

	r := gin.Default()
	r.LoadHTMLGlob("./templates/**/*")
	r.Static("/static", "./static")

	//==========================================================================
	// PAGES
	//==========================================================================

	r.GET("/", func(c *gin.Context) {
		c.HTML(200, "index.html", nil)
	})

	//==========================================================================
	// RUNNING
	//==========================================================================

	r.Run()

}